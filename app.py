import streamlit as st

# Настройка страницы
st.set_page_config(
    page_title="Калькулятор риска ЦНС (ЭКО)",
    page_icon="👶",
    layout="centered"
)

# Заголовок и описание
st.title("Прогноз риска поражения ЦНС")
st.subheader("у недоношенных детей, рожденных путем ЭКО")
st.markdown("""
Этот клинический калькулятор основан на результатах оценки маркеров ферроптоза и апоптоза.  
**Формула:** $K = Ферритин / Интерлейкин-18$
""")

st.divider()

# Создаем колонки для ввода данных, чтобы выглядело компактно
col1, col2 = st.columns(2)

with col1:
    ferritin = st.number_input(
        "Уровень ферритина:", 
        min_value=0.0, 
        value=0.0, 
        step=0.1,
        help="Введите концентрацию ферритина"
    )

with col2:
    il18 = st.number_input(
        "Уровень интерлейкина-18:", 
        min_value=0.0, 
        value=0.0, 
        step=0.1,
        help="Введите концентрацию IL-18"
    )

st.markdown("<br>", unsafe_allow_html=True)

# Кнопка расчета
if st.button("Рассчитать прогноз", type="primary", use_container_width=True):
    if ferritin == 0.0 and il18 == 0.0:
        st.info("Пожалуйста, введите значения ферритина и интерлейкина-18.")
    elif il18 == 0:
        st.error("Ошибка: Уровень интерлейкина-18 не может быть равен нулю (деление на ноль).")
    else:
        # Расчет коэффициента
        k = ferritin / il18
        
        # Вывод результата
        st.markdown(f"<h3 style='text-align: center;'>Коэффициент K = {k:.2f}</h3>", unsafe_allow_html=True)
        
        # Логика принятия решений (согласно ROC-анализу)
        if k <= 1.10:
            st.error("""
            #### 🚨 Высокий риск поражения ЦНС
            Коэффициент $K \le 1.10$ указывает на критически высокую вероятность развития гипоксически-ишемической энцефалопатии.
            * **Чувствительность метода:** 93.3%
            * Рекомендуется пристальное клиническое наблюдение и рассмотрение вопроса о нейропротекторной терапии.
            """)
        elif 1.10 < k <= 1.71:
            st.warning("""
            #### ⚠️ Умеренный риск (Серая зона)
            Коэффициент $K$ находится в диапазоне от 1.10 до 1.71. 
            * Требуется дополнительное наблюдение. Риск развития патологии ЦНС сохраняется.
            * Рекомендуется комплексная оценка дополнительных клинических и нейросонографических данных.
            """)
        else:
            st.success("""
            #### ✅ Низкий риск поражения ЦНС
            Коэффициент $K > 1.71$ указывает на низкую вероятность тяжелых поражений ЦНС.
            * **Прогностическая ценность отрицательного результата (NPV):** 80.0%
            """)

st.divider()

# Дополнительная информация в боковой панели (Сайдбар)
with st.sidebar:
    st.header("О методе")
    st.info("""
    Калькулятор разработан для прогнозирования перинатальных поражений ЦНС у недоношенных детей (срок гестации < 37 недель), рожденных по процедуре ЭКО.
    
    **Пороги отсечения (ROC-анализ):**
    * Высокий риск: $K \le 1.10$
    * Оптимальный порог (индекс Юдена): $K \le 1.71$
    """)
    st.caption("© 2024. Разработано в рамках диссертационного исследования.")

