import streamlit as st

# Настройка страницы
st.set_page_config(
    page_title="Калькулятор риска ЦНС (ЭКО)",
    layout="centered"
)

# Заголовок
st.title("Прогноз риска перинатального поражения ЦНС")
st.subheader("у недоношенных детей, зачатых с помощью ЭКО")

st.divider()

# Создаем колонки для ввода данных
col1, col2 = st.columns(2)

with col1:
    ferritin = st.number_input(
        "Уровень ферритина:", 
        min_value=0.0, 
        value=0.0, 
        step=0.1,
        help="Введите концентрацию ферритина"
    )

with col2:
    il18 = st.number_input(
        "Уровень интерлейкина-18:", 
        min_value=0.0, 
        value=0.0, 
        step=0.1,
        help="Введите концентрацию IL-18"
    )

st.markdown("<br>", unsafe_allow_html=True)

# Кнопка расчета
if st.button("Рассчитать прогноз", type="primary", use_container_width=True):
    if ferritin == 0.0 and il18 == 0.0:
        st.info("Пожалуйста, введите значения ферритина и интерлейкина-18.")
    elif il18 == 0:
        st.error("Ошибка: Уровень интерлейкина-18 не может быть равен нулю (деление на ноль).")
    else:
        # Расчет коэффициента
        k = ferritin / il18
        
        # Вывод результата
        st.markdown(f"<h3 style='text-align: center;'>Коэффициент K = {k:.2f}</h3>", unsafe_allow_html=True)
        
        # Логика принятия решений
        if k <= 1.10:
            st.error("""
            #### Высокий риск перинатального поражения ЦНС
            Коэффициент $K \le 1.10$ указывает на критически высокую вероятность развития гипоксически-ишемической энцефалопатии.
            * **Чувствительность метода:** 93.3%
            * Рекомендуется пристальное клиническое наблюдение и рассмотрение вопроса о нейропротекторной терапии.
            """)
        elif 1.10 < k <= 1.71:
            st.warning("""
            #### Умеренный риск (Серая зона)
            Коэффициент $K$ находится в диапазоне от 1.10 до 1.71. 
            * Требуется дополнительное наблюдение. Риск развития патологии ЦНС сохраняется.
            * Рекомендуется комплексная оценка дополнительных клинических и нейросонографических данных.
            """)
        else:
            st.success("""
            #### Низкий риск перинатального поражения ЦНС
            Коэффициент $K > 1.71$ указывает на низкую вероятность тяжелых перинатальных поражений ЦНС.
            * **Прогностическая ценность отрицательного результата (NPV):** 80.0%
            """)

st.divider()

